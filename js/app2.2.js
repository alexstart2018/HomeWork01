'use strict';
let x = prompt('Введите значение по оси х',"");
let y = prompt('Введите значение по оси y',"");
if (-1 * x + 1 * y  >= 0 && -1 * x - 1 * y <= 0 && 0 < y <= 1
    || 1 * x - 1 * y  >= 0 && -1 * x + 1 * y <= 0 && 0 > y >= -1) {
    alert( 'Точка попадает в область!' )
}
else {
    alert( 'Точка НЕ попадает в область' )
}